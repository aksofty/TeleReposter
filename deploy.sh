#!/bin/bash

# Останавливаем скрипт при любой ошибке
set -e

echo "Начинаем процесс деплоя..."

# 1. Скачиваем обновления из репозитория (если нужно)
git pull origin main

# 2. Собираем новые образы заранее. 
# Это самая долгая часть, и пока она идет, старые контейнеры работают.
echo "Сборка новых образов..."
docker-compose build --pull

# 3. Запускаем обновление. 
# Флаг --remove-orphans удалит старые сервисы, если вы их переименовали.
# Флаг -d (detach) позволяет Docker заменить контейнеры за доли секунды.
echo "Перезапуск сервисов..."
docker-compose up -d --remove-orphans

# 4. Очистка старых неиспользуемых образов (чтобы не забивать диск)
echo "Очистка старых ресурсов..."
docker image prune -f

echo "Деплой успешно завершен!"